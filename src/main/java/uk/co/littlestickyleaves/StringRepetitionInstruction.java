package uk.co.littlestickyleaves;

/**
 * POJO to hold info about what string to repeat and how often
 */
public class StringRepetitionInstruction {

    private String input;

    private int repeat;

    public String getInput() {
        return input;
    }

    public void setInput(String input) {
        this.input = input;
    }

    public int getRepeat() {
        return repeat;
    }

    public void setRepeat(int repeat) {
        this.repeat = repeat;
    }
}
